const { ValidationError } = require("../errors/AppError");

const VALID_TYPES = ["IN", "OUT", "ADJUST"];

const validateProductIdParam = (req, res, next) => {
  const productId = Number(req.params.productId);

  if (!Number.isInteger(productId) || productId <= 0) {
    return next(new ValidationError("productId must be a positive integer"));
  }

  req.params.productId = productId;
  next();
};

const validateCreateStockTransaction = (req, res, next) => {
  const { type, quantity, note } = req.body || {};

  if (type === "SALE") {
    return next(
      new ValidationError("type SALE is system-generated by the sales module, not settable here")
    );
  }

  if (!VALID_TYPES.includes(type)) {
    return next(new ValidationError(`type must be one of ${VALID_TYPES.join(", ")}`));
  }

  if (!Number.isInteger(quantity) || quantity === 0) {
    return next(new ValidationError("quantity must be a nonzero integer"));
  }

  if ((type === "IN" || type === "OUT") && quantity < 0) {
    return next(new ValidationError(`quantity must be a positive integer for type ${type}`));
  }

  if (note !== undefined && note !== null && typeof note !== "string") {
    return next(new ValidationError("note must be a string"));
  }

  next();
};

module.exports = { validateCreateStockTransaction, validateProductIdParam };
